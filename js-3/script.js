// Завдання 3: Перевірка числа на паліндром
// =============================================
console.log("3. Перевірка числа на паліндром");

function isPalindrome(num) {
    // Перетворюємо число в рядок
    let str = num.toString();
    // Перевертаємо рядок
    let reversed = str.split('').reverse().join('');
    
    // Порівнюємо
    return str === reversed;
}

// Тестування з прикладом
let testNumber = 12321;
console.log(`Число ${testNumber} — паліндром? ${isPalindrome(testNumber) ? "Так" : "Ні"}`);

// Інтерактивна перевірка
let userInput = prompt("Введіть число для перевірки на паліндром:");

if (userInput !== null) {
    let number = Number(userInput);
    
    if (!isNaN(number)) {
        if (isPalindrome(number)) {
            alert(`Число ${number} є ПАЛІНДРОМОМ! ✅`);
        } else {
            alert(`Число ${number} НЕ є паліндромом. ❌`);
        }
    } else {
        alert("Помилка! Введіть коректне число.");
    }
}