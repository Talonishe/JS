// =============================================
// Завдання №1: Парне чи непарне число
// =============================================
function checkEvenOdd() {
    let input = prompt("Введіть будь-яке ціле число:");

    // Перетворюємо введене значення на число
    let number = Number(input);

    // Перевірка, чи користувач ввів саме число
    if (isNaN(number)) {
        alert("Помилка! Ви ввели не число.");
        return;
    }

    // Перевірка на парність
    if (number % 2 === 0) {
        alert(`Число ${number} є ПАРНИМ`);
    } else {
        alert(`Число ${number} є НЕПАРНИМ`);
    }
}

// Запускаємо перше завдання
console.log("=== Завдання 1 ===");
checkEvenOdd();


